import SwiftUI
import AppKit

/// A view modifier that provides access to the underlying NSWindow
struct WindowAccessor: NSViewRepresentable {
    @Binding var window: NSWindow?

    func makeNSView(context: Context) -> NSView {
        let view = NSView()
        DispatchQueue.main.async {
            self.window = view.window
        }
        return view
    }

    func updateNSView(_ nsView: NSView, context: Context) {
        DispatchQueue.main.async {
            self.window = nsView.window
        }
    }
}

/// View extension to easily access the window
extension View {
    func accessWindow(window: Binding<NSWindow?>) -> some View {
        self.background(WindowAccessor(window: window))
    }
}
